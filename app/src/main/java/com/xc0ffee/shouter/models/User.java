package com.xc0ffee.shouter.models;

public class User {

    private String name;
    private long id;
    private String screen_name;
    private String profile_image_url;

    public String getName() {
        return name;
    }

    public long getId() {
        return id;
    }

    public String getScreenName() {
        return screen_name;
    }

    public String getProfileImageUrl() {
        return profile_image_url;
    }
}
